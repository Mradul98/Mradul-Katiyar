public class Array {  
    public static void main(String[] args) {      
          
        //Initialize array   
        String [] arr = {"Mradul", "Katiyar"};  
          
          
        System.out.println("Elements of given array: ");  
        //Loop through the array by incrementing value of i   
        for (int i = 0; i < arr.length; i++) {   
            System.out.print(arr[i] + " ");   
        }    
    }  
}  